class DateTimePart {
    static get yyyy() {
        return "yyyy";
    }

    static get MM() {
        return "MM";
    }

    static get dd() {
        return "dd";
    }

    static get HH() {
        return "HH";
    }

    static get mm() {
        return "mm";
    }

    static get ss() {
        return "ss";
    }

    static get SSS() {
        return "SSS";
    }
}

export default DateTimePart;